#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const workspaceRoot = process.cwd();
const checkOnly = process.argv.includes('--check');

const modules = [
  {
    source: 'packages/os-scripting/src/plugin-runtime/stack-bootstrap.vm.js',
    target: 'packages/os-scripting/src/plugin-runtime/stackBootstrapSource.generated.ts',
  },
  {
    source: 'packages/os-ui-cards/src/runtime-packages/ui.package.vm.js',
    target: 'packages/os-ui-cards/src/runtime-packages/uiPackageSource.generated.ts',
  },
  {
    source: 'packages/os-kanban/src/runtime-packages/kanban.package.vm.js',
    target: 'packages/os-kanban/src/runtime-packages/kanbanPackageSource.generated.ts',
  },
];

function toPosixPath(value) {
  return value.split(path.sep).join('/');
}

async function buildGeneratedModule({ source, target }) {
  const sourcePath = path.join(workspaceRoot, source);
  const targetPath = path.join(workspaceRoot, target);
  const sourceText = await readFile(sourcePath, 'utf8');
  const relativeSource = toPosixPath(path.relative(path.dirname(targetPath), sourcePath));
  const generated = [
    '// Generated by scripts/packages/generate-vm-source-modules.mjs.',
    `// Source: ${relativeSource}`,
    '// Do not edit by hand.',
    '',
    `const source = ${JSON.stringify(sourceText)};`,
    '',
    'export default source;',
    '',
  ].join('\n');

  return { targetPath, target, generated };
}

let staleCount = 0;

for (const entry of modules) {
  const { targetPath, target, generated } = await buildGeneratedModule(entry);
  if (checkOnly) {
    const existing = await readFile(targetPath, 'utf8').catch(() => null);
    if (existing !== generated) {
      staleCount += 1;
      console.error(`${target} is stale; run npm run generate:vm-sources`);
    }
    continue;
  }

  await mkdir(path.dirname(targetPath), { recursive: true });
  await writeFile(targetPath, generated, 'utf8');
  console.log(`wrote ${target}`);
}

if (staleCount > 0) {
  process.exit(1);
}

if (checkOnly) {
  console.log('VM source modules are up to date.');
}
